#EL SIGUIENTE PROGRAMA COMPARA TRES NUMEROS (CADA UNO EN UNA VARIABLE A,B Y C )
#SI SON IGUALES, SALTA A UNA "SUBRUTINA"
# Y INCREMENTA R4, EN EL CUAL VAMOS A GUARDAR EL RESULTADO
# LA ULTIMA INSTRUCCION DEL PROGRAMA LO QUE HACE ES CARGAR EL VALOR DE R4 (CANT DE VECES REPETIDAS) EN LA VARIABLE D
# AL FINALIZAR EL PROGRAMA, EN LA VARIABLE D VAMOS A TENER LA CANTIDAD DE VECES QUE LAS VAROABLES TIENEN EL MISMO VALOR
.data
A: .word 2
B: .word 2
C: .word 3
D: .word 0
.code
#CARGAMOS LAS VARIABLES EN REGISTROS
ld r1,A (r0)
ld r2,B(r0)
ld r3,C(r0)
ld r4,D(r0)

#COMPARAMOS REGISTROS
#R1 = A
#R2 = B
#R3 = C

comp1: beq r1, r2, iguales1 #CMP R1, R2
comp2: beq r1, r3, iguales2 #CMP R1, R3
comp3: beq r2, r3, iguales3 #CMP R2, R3

resultado: sd r4,D(r0) #GUARDAMOS EL RESULTADO EN LA VARIABLE D
           halt        #FINALIZA EL PROGRAMA



#SI LOS REGISTROS SON IGUALES, VA A ENTRAR EN LA """SUBRITINA""
#Y VA A RETORNAR DONDE CORRESPONDA, PARA SEGUIR EJECUTANDO EL PROGRAMA
iguales1: daddi r4, r4,1
          j comp2

iguales2: daddi r4,r4,1
          j comp3

iguales3: daddi r4, r4,1
          j resultado
